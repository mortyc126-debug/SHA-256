def gf2_poly_mul(a, b):
    """Multiply two GF(2) polynomials (represented as integers, bit i = coeff of x^i)."""
    result = 0
    while b:
        if b & 1:
            result ^= a
        a <<= 1
        b >>= 1
    return result

def gf2_poly_mod(a, m):
    """a mod m over GF(2)."""
    deg_m = m.bit_length() - 1
    while a.bit_length() - 1 >= deg_m and a > 0:
        a ^= m << (a.bit_length() - 1 - deg_m)
    return a

def gf2_poly_gcd(a, b):
    """GCD of two GF(2) polynomials."""
    while b:
        a, b = b, gf2_poly_mod(a, b)
    return a

def gf2_poly_deg(p):
    """Degree of GF(2) polynomial."""
    if p == 0: return -1
    return p.bit_length() - 1

def poly_to_str(p):
    """Pretty-print GF(2) polynomial."""
    if p == 0: return "0"
    terms = []
    for i in range(p.bit_length()):
        if (p >> i) & 1:
            if i == 0: terms.append("1")
            elif i == 1: terms.append("x")
            else: terms.append(f"x^{i}")
    return " + ".join(reversed(terms))

