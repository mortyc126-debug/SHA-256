def test_polynomial_attack():
    """Can polynomial structure be used for attack?"""
    print(f"\n{'='*60}")
    print(f"POLYNOMIAL ATTACK IMPLICATIONS")
    print(f"{'='*60}")

    # If Σ functions are invertible: no null differences
    # But they might have LOW-WEIGHT differences:
    # δe such that HW(Σ₁(δe)) is small → small contribution to δT1

    # Find: which δe minimizes HW(Σ₁(δe))?
    print(f"\n  Minimum-weight Σ₁ outputs:")
    best = []
    for hw_target in range(1, 8):
        count = 0
        for delta_e in range(1, 2**20):  # Sample
            if hw(delta_e) > 5: continue  # Sparse input
            s = sigma1(delta_e)
            if hw(s) <= hw_target:
                count += 1
                if len(best) < 5:
                    best.append((hw(s), hw(delta_e), delta_e, s))

    best.sort()
    print(f"    HW(Σ₁(δe)) | HW(δe) | δe")
    print(f"    " + "-" * 40)
    for hw_out, hw_in, de, s in best[:8]:
        print(f"    {hw_out:>11} | {hw_in:>6} | 0x{de:08x} → 0x{s:08x}")

    # Same for Σ₀
    print(f"\n  Minimum-weight Σ₀ outputs:")
    best0 = []
    for delta_a in range(1, 2**20):
        if hw(delta_a) > 5: continue
        s = sigma0(delta_a)
        if hw(s) <= 5:
            best0.append((hw(s), hw(delta_a), delta_a, s))

    best0.sort()
    for hw_out, hw_in, da, s in best0[:8]:
        print(f"    HW(Σ₀(δa))={hw_out}, HW(δa)={hw_in}: "
              f"0x{da:08x} → 0x{s:08x}")

    # What's the minimum WEIGHT RATIO?
    print(f"\n  Weight expansion ratio HW(Σ(δ))/HW(δ):")
    ratios_1 = []; ratios_0 = []
    for _ in range(10000):
        de = random.randint(1, MASK)
        ratios_1.append(hw(sigma1(de)) / max(hw(de), 1))
        ratios_0.append(hw(sigma0(de)) / max(hw(de), 1))

    r1 = np.array(ratios_1); r0 = np.array(ratios_0)
    print(f"    Σ₁: min_ratio={r1.min():.3f}, avg={r1.mean():.3f}")
    print(f"    Σ₀: min_ratio={r0.min():.3f}, avg={r0.mean():.3f}")

