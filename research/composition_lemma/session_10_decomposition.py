"""
Session 10: Extract minimal ideal decomposition of D^4.

After Session 9 found D^4 NOT simple (proper ideals of dim 45, 55, 65, 75 exist),
extract complete decomposition:

1. For each basis element x_i: compute ideal_i = ideal generated by x_i.
2. Cluster: which basis elements give same/equivalent ideal?
3. Find minimal distinct ideals I_1, I_2, ..., I_m.
4. Test direct sum: dim(I_1) + ... + dim(I_m) = 264?
   If yes → D^4 = ⊕ I_i (semisimple structure).
5. Compute brackets [I_a, I_b] for a ≠ b. If all zero → strong direct sum (each I_i is ideal).

Optimization: precompute adjoint matrices ad_i (264 × 264 over F_2),
avoid einsum by using direct matrix-vector multiplies.
"""

