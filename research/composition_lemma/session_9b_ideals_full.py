def setup_basis_resolver(D4):
    n = 264
    BT = np.array([m.flatten() for m in D4], dtype=np.uint8)
    work = BT.copy()
    T = np.eye(n, dtype=np.uint8)
    pivot_cols = []
    row = 0
    for col in range(BT.shape[1]):
        if row >= n:
            break
        pivots_in_col = np.where(work[row:, col] == 1)[0]
        if len(pivots_in_col) == 0:
            continue
        pivot = row + pivots_in_col[0]
        if pivot != row:
            work[[row, pivot]] = work[[pivot, row]]
            T[[row, pivot]] = T[[pivot, row]]
        ones_mask = work[:, col] == 1
        ones_mask[row] = False
        if ones_mask.any():
            work[ones_mask] ^= work[row]
            T[ones_mask] ^= T[row]
        pivot_cols.append(col)
        row += 1
    return np.array(pivot_cols), T


def compute_ideal_orbit_fast(x_coeffs, C, max_iter=20):
    """Optimized: for each y in frontier, all 264 brackets via einsum.
    [x_i, y]_coeffs = einsum('ijk,j->ik', C, y) over F_2.
    """
    n = 264
    ideal = GF2Basis(n)
    ideal.add(x_coeffs)
    frontier = [x_coeffs.copy()]
    for it in range(max_iter):
        new_elements = []
        for y in frontier:
            # All [x_i, y] for i = 0..263 — shape (264, 264)
            # ([x_i, y])[k] = sum_j y[j] * C[i, j, k]
            brackets_matrix = (np.einsum('ijk,j->ik', C, y.astype(np.int32)) & 1).astype(np.uint8)
            for k in range(n):
                row = brackets_matrix[k]
                if row.sum() == 0:
                    continue
                added, _ = ideal.add(row)
                if added:
                    new_elements.append(row.copy())
        frontier = new_elements
        if not frontier:
            break
    return ideal


